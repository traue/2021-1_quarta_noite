void main() {
  //desvio condicional com switch..case

  String time = "Bragantino";

  switch(time) {
    case "São Paulo":
      print("São Paulino");
      break;
    case "Corinthians":
      print("Itaquerão!!");
      break;
    case "Bragantino":
      print("É da terra da linguiça");
      break;
    case "Palmeiras":
      print("Grande palestra");
      break;
    default:
      print("Sabe-se lá...");
      break;
  }
}
